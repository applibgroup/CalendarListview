package com.andexert.calendarlistview.library;

/**
 * DayPickerView的自定义属性
 */
public class DayPickerViewAttrs {

    /**
     * 当前日期的文字颜色
     */
    public static final String COLOR_CURRENT_DAY = "colorCurrentDay";
    /**
     * 点击日期的圆形或矩形指示器的背景色
     */
    public static final String COLOR_SELECTED_DAY_BACKGROUND = "colorSelectedDayBackground";
    /**
     * 点击日期的文字颜色
     */
    public static final String COLOR_SELECTED_DAY_TEXT = "colorSelectedDayText";
    /**
     * 当前月份过去日期的文字颜色
     */
    public static final String COLOR_PREVIOUS_DAY = "colorPreviousDay";
    /**
     * 所有未过日期的文字颜色
     */
    public static final String COLOR_NORMAL_DAY = "colorNormalDay";
    /**
     * 每个月的年月标题的文字颜色
     */
    public static final String COLOR_MONTH_NAME = "colorMonthName";
    /**
     * 每个月的星期的文字颜色
     */
    public static final String COLOR_DAY_NAME = "colorDayName";
    /**
     * 日期文字大小
     */
    public static final String TEXT_SIZE_DAY = "textSizeDay";
    /**
     * 每个月的年月标题的文字大小
     */
    public static final String TEXT_SIZE_MONTH = "textSizeMonth";
    /**
     * 每个月的星期的文字大小
     */
    public static final String TEXT_SIZE_DAY_NAME = "textSizeDayName";
    /**
     * 每个月的年月标题的高度
     */
    public static final String HEADER_MONTH_HEIGHT = "headerMonthHeight";
    /**
     * 点击日期指示器的半径尺寸
     */
    public static final String SELECTED_DAY_RADIUS = "selectedDayRadius";
    /**
     * 每个月/行的高度
     */
    public static final String CALENDAR_HEIGHT = "calendarHeight";
    /**
     * 启用当前月份的过去日期
     */
    public static final String ENABLE_PREVIOUS_DAY = "enablePreviousDay";
    /**
     * 默认情况下选择当前日期
     */
    public static final String CURRENT_DAY_SELECTED = "currentDaySelected";
    /**
     * 点击日期指示器的形状，ture为圆角矩形，fase为圆形
     */
    public static final String DRAW_ROUND_RECT = "drawRoundRect";
    /**
     * 开始显示的月份
     */
    public static final String FIRST_MONTH = "firstMonth";
    /**
     * 最后显示的月份
     */
    public static final String LAST_MONTH = "lastMonth";

}
